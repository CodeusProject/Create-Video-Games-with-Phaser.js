var myAge = 23;
var earlyYears = 2;
earlyYears *= 10.5;
//laterYears is the number of years remaining after we have counted the first two higher-value years.
laterYears = myAge - 2;
//Counting the laterYears as dog years.
laterYears = laterYears * 4;
//Total dog age
myAgeInDogYears = earlyYears + laterYears;
myName = 'Gabriel'.toLowerCase();
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)
